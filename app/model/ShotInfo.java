package model;

public class ShotInfo {
	private String shot;
	private String shotBy;
	
	public String getShot() {
		return shot;
	}
	public void setShot(String shot) {
		this.shot = shot;
	}
	public String getShotBy() {
		return shotBy;
	}
	public void setShotBy(String shotBy) {
		this.shotBy = shotBy;
	}

}
